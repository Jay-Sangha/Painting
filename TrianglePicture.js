// Sunset landscape with initials "JS" — 30+ colored triangles.
// Coordinates are in WebGL clip space [-1, 1].

function buildTrianglePicture() {
  const T = (x1, y1, x2, y2, x3, y3, r, g, b, a) => ({
    x1, y1, x2, y2, x3, y3,
    r: r / 255, g: g / 255, b: b / 255, a: a !== undefined ? a : 1
  });

  return [
    // Sky gradient (warm sunset)
    T(-1, 1, 1, 1, 1, 0.35, 255, 140, 60),
    T(-1, 1, 1, 0.35, -1, 0.35, 120, 60, 140),
    T(-1, 0.35, 1, 0.35, 1, -0.1, 80, 40, 100),
    T(-1, 0.35, 1, -0.1, -1, -0.1, 40, 20, 60),

    // Sun
    T(0.55, 0.55, 0.72, 0.62, 0.68, 0.42, 255, 220, 80),
    T(0.55, 0.55, 0.68, 0.42, 0.48, 0.48, 255, 200, 50),
    T(0.48, 0.48, 0.68, 0.42, 0.52, 0.28, 255, 180, 40),

    // Distant mountains (back)
    T(-1, 0.05, -0.2, 0.55, 0.15, 0.05, 70, 50, 90),
    T(0.15, 0.05, 0.5, 0.5, 1, 0.05, 60, 45, 80),
    T(-0.2, 0.55, 0.5, 0.5, 0.15, 0.05, 55, 40, 75),

    // Near mountains
    T(-1, -0.15, -0.45, 0.35, 0.0, -0.15, 35, 55, 70),
    T(0.0, -0.15, 0.35, 0.38, 0.55, -0.15, 30, 50, 65),
    T(0.55, -0.15, 1, 0.32, 1, -0.15, 25, 45, 60),
    T(-0.45, 0.35, 0.35, 0.38, 0.0, -0.15, 40, 60, 75),

    // Ground / water reflection
    T(-1, -0.15, 1, -0.15, 1, -1, 15, 25, 45),
    T(-1, -0.15, 1, -1, -1, -1, 10, 18, 35),
    T(-0.3, -0.15, 0.25, -0.5, 0.1, -0.15, 25, 40, 55),

    // Pine trees (left)
    T(-0.85, -0.12, -0.78, 0.15, -0.72, -0.12, 20, 70, 35),
    T(-0.78, 0.15, -0.7, -0.12, -0.72, -0.12, 15, 60, 30),
    T(-0.72, -0.12, -0.65, 0.22, -0.58, -0.12, 18, 75, 38),
    T(-0.65, 0.22, -0.52, -0.12, -0.58, -0.12, 12, 55, 28),
    T(-0.58, -0.12, -0.5, 0.28, -0.42, -0.12, 20, 80, 40),
    T(-0.5, 0.28, -0.38, -0.12, -0.42, -0.12, 14, 58, 32),

    // Extra shoreline detail
    T(0.6, -0.15, 0.75, -0.35, 0.9, -0.15, 30, 50, 70),
    T(0.35, -0.15, 0.5, -0.42, 0.65, -0.15, 28, 48, 68)
  ];
}

function renderTrianglePicture(gl, program, uniforms, triangles) {
  for (let i = 0; i < triangles.length; i++) {
    const t = triangles[i];
    drawTriangle(
      gl, program, uniforms,
      t.x1, t.y1, t.x2, t.y2, t.x3, t.y3,
      t.r, t.g, t.b, t.a
    );
  }
}
